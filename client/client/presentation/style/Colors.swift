import SwiftUI

public enum Colors {
    public static let primary      = Color.accentColor
    public static let onPrimary    = Color(.white)
    
    public static let secondary    = Color(.systemGreen)
    public static let onSecondary  = Color(.white)
    
    public static let background   = Color(.systemBackground)
    public static let onBackground = Color(.label)
    
    public static let surface      = Color(.secondarySystemBackground)
    public static let onSurface    = Color(.label)
    
    public static let error        = Color(.systemRed)
    public static let onError      = Color(.white)
}
